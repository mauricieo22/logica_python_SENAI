def celsius_fahrenheit(c:float) -> float:
    """Converte temperatura de Celsius para Fahrenheit.
    Fórmula: F = C * 9/5 + 32
    """
    return c * 9/5 + 32

def metros_quilometros(m:float) -> float:
    """Converte distância em metros para quilômetros.
    1 km = 1000 m
    """
    return m / 1000